    public static int Count(int[][] maze, int i, int j, int x, int y) {

        if (maze == null || maze.length == 0 || maze[i][j] == 0 || maze[x][y] == 0) {
            return 0;
        }

        return Route(maze, i, j, x, y, new boolean[maze.length][maze[0].length]);
    }

    private static int Route(int[][] maze, int i, int j, int x, int y, boolean visited[][]) {

        if (i == x && j == y) {
            return 1;
        }

        int count = 0;

        visited[i][j] = true;

        if (Valid(i, j, R, C) && maze[i][j] == 1) {

            if (i + 1 < R && !visited[i + 1][j]) {
                count += Route(maze, i + 1, j, x, y, visited);
            }

            if (i - 1 >= 0 && !visited[i - 1][j]) {
                count += Route(maze, i - 1, j, x, y, visited);
            }

            if (j + 1 < C && !visited[i][j + 1]) {
                count += Route(maze, i, j + 1, x, y, visited);
            }

            if (j + 1 < C && i + 1 < R && !visited[i + 1][j + 1]) {
                count += Route(maze, i + 1, j + 1, x, y, visited);
            }
        }

        visited[i][j] = false;

        return count;
    }

    private static boolean Valid(int x, int y, int R, int C) {
        return !(x < 0 || y < 0 || x >= R || y >= C);
    }

